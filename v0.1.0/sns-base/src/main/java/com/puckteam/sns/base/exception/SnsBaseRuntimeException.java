public class SnsBaseRuntimeException extends RuntimeException{


    public SnsBaseRuntimeException() {
        super();
    }

    public SnsBaseRuntimeException(String message) {
        super(message);
    }

    public SnsBaseRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public SnsBaseRuntimeException(Throwable cause) {
        super(cause);
    }
}
